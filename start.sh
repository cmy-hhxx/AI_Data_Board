#!/bin/bash
set -e

# ============================================================
#  AI Data Board — 一键启动脚本（macOS / Linux）
#  用法:
#    ./start.sh              正常启动
#    ./start.sh --verbose    详细模式（打印每一步，方便调试）
#    ./start.sh -v           同上
# ============================================================

# ------------------------------------------------------------------
# 解析参数
# ------------------------------------------------------------------
VERBOSE=false
while [[ $# -gt 0 ]]; do
  case "$1" in
    --verbose|-v)
      VERBOSE=true
      shift
      ;;
    *)
      echo "[ERROR] 未知选项: $1"
      echo "用法: $0 [--verbose|-v]"
      exit 1
      ;;
  esac
done

# ------------------------------------------------------------------
# 辅助函数
# ------------------------------------------------------------------
info()  { echo -e "$1"; }
detail() {
  if [[ "$VERBOSE" == true ]]; then
    echo -e "  |-> $1"
  fi
}
step()  {
  local label="$1"
  if [[ "$VERBOSE" == true ]]; then
    echo ""
    echo " +------------------------------------------------------"
    echo " | [$(date '+%H:%M:%S')] $label"
    echo " +------------------------------------------------------"
  else
    echo "  -> $label"
  fi
}

# ------------------------------------------------------------------
# Banner
# ------------------------------------------------------------------
echo ""
echo "  +-----------------------------------------------+"
echo "  |       AI Data Board -- 启动中...               |"
echo "  +-----------------------------------------------+"
echo ""

# ------------------------------------------------------------------
# Step 1 — 环境检查
# ------------------------------------------------------------------
step "检查运行环境"

detail "检查 Node.js..."
if ! command -v node &>/dev/null; then
  echo "[ERROR] 未找到 Node.js，请先安装 Node.js >= 18"
  echo "   推荐使用 nvm: curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.40.1/install.sh | bash"
  exit 1
fi
NODE_VER=$(node -v)
detail "Node.js 版本: $NODE_VER"

detail "检查 pnpm..."
if ! command -v pnpm &>/dev/null; then
  echo "[ERROR] 未找到 pnpm，请先安装: npm install -g pnpm"
  exit 1
fi
PNPM_VER=$(pnpm -v)
detail "pnpm 版本: $PNPM_VER"

# ------------------------------------------------------------------
# Step 2 — 网络代理（可选）
#    仅当环境变量未设置时尝试自动检测本地代理
#    Supabase 在某些网络环境下需要代理才能连接
# ------------------------------------------------------------------
step "网络配置"
if [[ -z "$https_proxy" && -z "$http_proxy" ]]; then
  if command -v nc &>/dev/null; then
    if nc -z -G 1 127.0.0.1 7897 2>/dev/null; then
      detail "检测到本地代理 (127.0.0.1:7897)，已自动启用"
      export https_proxy=http://127.0.0.1:7897
      export http_proxy=http://127.0.0.1:7897
      export all_proxy=socks5://127.0.0.1:7897
    else
      detail "未检测到本地代理，跳过（如需要请设置 https_proxy 环境变量）"
    fi
  else
    detail "nc 命令不可用，跳过代理检测（如需要请设置 https_proxy 环境变量）"
  fi
else
  detail "已设置代理: https_proxy=${https_proxy:-<未设置>}"
fi

# ------------------------------------------------------------------
# Step 3 — 安装依赖（如需）
# ------------------------------------------------------------------
step "安装依赖"
if [[ ! -d "node_modules" ]]; then
  detail "首次运行，安装依赖..."
  pnpm install
  detail "依赖安装完成"
else
  detail "依赖已存在"
fi

# ------------------------------------------------------------------
# Step 4 — 清理旧进程
# ------------------------------------------------------------------
step "清理旧进程"
for port in 8787 5173; do
  local_pid=$(lsof -ti:$port 2>/dev/null || true)
  if [[ -n "$local_pid" ]]; then
    kill $local_pid 2>/dev/null || true
    detail "已释放端口 $port (PID $local_pid)"
  fi
done

# ------------------------------------------------------------------
# 导出环境变量（子进程可读取）
# ------------------------------------------------------------------
export VERBOSE

# ------------------------------------------------------------------
# Step 5 — 启动后端
# ------------------------------------------------------------------
step "启动后端 (port 8787)"
pnpm --filter backend dev &
BACKEND_PID=$!
detail "后端 PID: $BACKEND_PID"

# ------------------------------------------------------------------
# Step 6 — 启动前端
# ------------------------------------------------------------------
step "启动前端 (port 5173)"
pnpm --filter frontend dev &
FRONTEND_PID=$!
detail "前端 PID: $FRONTEND_PID"

# ------------------------------------------------------------------
# 输出结果
# ------------------------------------------------------------------
echo ""
echo "  +-----------------------------------------------+"
echo "  |  AI Data Board 启动完成！                     |"
echo "  |                                               |"
echo "  |  前端:  http://localhost:5173                 |"
if [[ "$VERBOSE" == true ]]; then
  echo "  |  后端:  http://localhost:8787                 |"
fi
echo "  |                                               |"
echo "  |  按 Ctrl+C 停止所有服务                        |"
echo "  +-----------------------------------------------+"
echo ""

# ------------------------------------------------------------------
# 等待任一进程退出
# ------------------------------------------------------------------
wait